class CourseInfo:

    def __init__(self, course_name, instructor, start_time, end_time, tas_per_course, students_per_course, labs_per_course):
        self.course_name = course_name
        self.instructor = instructor
        self.start_time = start_time
        self.end_time = end_time
        self.tas_per_course = tas_per_course
        self.students_per_course = students_per_course
        self.labs_per_course = labs_per_course

